package JavaPodstawyWarunki;

public class ZadanieB {
    public static void main(String[] args) {

        int number = 200;

        switch (number) {
            case 10: {
                System.out.println("Liczba to 10");
                break;
            }
            case 100: {
                System.out.println("Liczba to 100");
                break;
            }
            default: {
                System.out.println("Liczba jest rozna od 10 i 100");
                break;
            }
        }
    }
}
